package pojo;

public class CustomerDetailsAppium 
{
	private String CourseName;
	private String PurchasedDate;
	private String Amount;
	private String Location;
	private String studentName;

	public String getStudentName() {	return studentName; }

	public void setStudentName(String studentName) {	this.studentName = studentName;}

	public String getCourseName() {	return CourseName;}

	public void setCourseName(String courseName) {	CourseName = courseName; }

	public String getPurchasedDate() {	return PurchasedDate; }

	public void setPurchasedDate(String purchasedDate) {	PurchasedDate = purchasedDate; }

	public String getAmount() {	return Amount; }

	public void setAmount(String amount) {	Amount = amount; }

	public String getLocation() { return Location; }

	public void setLocation(String location) {	Location = location; }


}
